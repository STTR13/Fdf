#include "../geometry.h"

bool			isnullvector(ve v)
{
	if (v->x == 0 && v->y == 0 && v->z == 0)
		return (true);
	return (false);
}

double 					norm(ve v)
{
	return (sqrt(pow(v->x, 2) + pow(v->y, 2) + pow(v->z, 2)));
}
