#include "../geometry.h"

matrix			adj(matrix m)
{
	matrix rm;

	rm[0][0] = m[1][1] * m[2][2] - m[1][2] * m[2][1];
	rm[0][1] = - (m[0][1] * m[2][2] - m[0][2] * m[2][1]);
	rm[0][2] = m[0][1] * m[1][2] - m[0][2] * m[1][1];
	rm[1][0] = - (m[1][0] * m[2][2] - m[1][2] * m[2][0]);
	rm[1][1] = m[0][0] * m[2][2] - m[0][2] * m[2][0];
	rm[1][2] = - (m[0][0] * m[1][2] - m[0][2] * m[1][0]);
	rm[2][0] = m[1][0] * m[2][1] - m[1][1] * m[2][0];
	rm[2][1] = - (m[0][0] * m[2][1] - m[0][1] * m[2][0]);
	rm[2][2] = m[0][0] * m[1][1] - m[0][1] * m[1][0];
	return (rm);
}

double			det(matrix m)
{
	adj = adj(m);
	return (adj[0][0] * m[0][0] + adj[0][1] * m[1][0] + adj[0][2] * m[2][0]);
}

matrix			inv(matrix m)
{
	adj = adj(m);
	det = adj[0][0] * m[0][0] + adj[0][1] * m[1][0] + adj[0][2] * m[2][0];
	return ((det == 0) ? nullvector() : scal_m(dot_mv(adj, m), 1 / det));
}
